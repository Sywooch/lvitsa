    /**
     * Displays homepage.
     *
     * @return mixed
     */
    public function actionIndex()
    {        
        $latestDresses=new ActiveDataProvider([
            'query'=>Products::find()->joinWith('image')->where(['latest'=>true,'visible'=>true]),
            'pagination' => [
	        'pageSize' => 40,
	    	],
            ]);
        
        $recommendDresses=new ActiveDataProvider([
            'query'=>Products::find()->joinWith('image')->where(['recommend'=>true,'visible'=>true]),
            'pagination' => [
	        'pageSize' => 40,
	    	],
            ]);

        $latestNews= new ActiveDataProvider([
            'query' => News::find()->orderBy('created_at DESC')->limit(2),
        ]);

        return $this->render('index', ['latestDresses'=>$latestDresses,
                                        'recommendDresses'=>$recommendDresses,
                                        'latestNews'=>$latestNews,
                                       ]);
    }

    /**
     * Logs in a user.
     *
     * @return mixed
     */
    public function actionLogin()
    {
        if (!\Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        } else {
            return $this->render('login', [
                'model' => $model,
            ]);
        }
    }

    /**
     * Logs out the current user.
     *
     * @return mixed
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }

    /**
     * Displays contact page.
